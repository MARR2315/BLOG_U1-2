							<p>1950-1960<br>No es secreto para nadie que durante la década de los 50’, los sistemas de información eran bastantes simples y primitivos, y esto se debía a la tecnología de la época.Por ello la principal función de dichos sistemas era asistir a los trabajadores de diferentes áreas ayudándoles en sus distintas tareas, siendo algunas de estas el procesar transacciones, mantener los registros y llevar la contabilidad básica.</p>
							<p>De esta forma, el SI (Sistema de información), se utilizaron principalmente en el procesamiento electrónico de datos. Se empleaban ordenadores para registrar, clasificar y manipular datos.  A esto se le puede llamar “sistemas de información de base de datos”.</p>
							<p>1960-1970<br>En esta época surgieron los sistemas de información gerencial, también conocidos como MSI, los cuales presentaron la novedad de convertir todos los datos almacenados en información útil, lo que ayudó en la toma de decisiones empresariales.</p>		
							<p>Los MSI presentaban su información en forma de pantalla e informes pre-especificados, los cales respaldaban la toma de decisiones empresariales.De los ejemplos de MSI, podemos mencionar a los “Sistemas de informes de tendencias de costos”, los “análisis de ventas” y el “rendimiento de producción”.</p>
