							<p><b>Sistemas de Procesamiento de Transacciones (TPS).</b> También conocidos como sistemas de gestión operativa, recopilan la información pertinente a las transacciones de la organización, es decir, de su funcionamiento.</p>
							<p><b>Sistemas de Información Ejecutiva (EIS).</b> Monitoriza las variables gerenciales de un área específica de la organización, a partir de la información interna y externa de la misma.</p>
							<p><b>Sistemas de Información Gerencial (MIS).</b> Contemplan la información general de la organización y la comprenden como un todo.</p>
